package java_core_bai3;

public class MonThi {
	private String tenMon;
	private int diem;
	
	public MonThi(String tenMon, int diem) {
		this.tenMon = tenMon;
		this.diem = diem;
	}
	public String getTenMon() {
		return tenMon;
	}
	public void setTenMon(String tenMon) {
		this.tenMon = tenMon;
	}
	public int getDiem() {
		return diem;
	}
	public void setDiem(int diem) {
		this.diem = diem;
	}
	
}
